<h4><a name="elements"></a>Elements</h4>

<p>
    Elements is a media storage solution that also offers a web interfact to browse images and videos. An Elements storage disk can connect to the service using an API token.
</p>

<p>
    In order to enable all features of BIIGLE, the Elements server should be configured for cross-origin resource sharing (CORS). Take a look at the <a href="{{route('manual-tutorials', ['volumes', 'remote-locations'])}}#cors">remote locations</a> article for more information on CORS and how it must be configured for BIIGLE.
</p>

<p>
    An Elements storage disk has the following options:
</p>

<dl>
    <dt>Base URI</dt>
    <dd>
        <p>
            This is the URI/URL of the Elements server.
        </p>
    </dd>

    <dt>Token</dt>
    <dd>
        <p>
            The API token of your user account. You can create a new token in the "API" tab of your user profile.
        </p>
    </dd>
</dl>
